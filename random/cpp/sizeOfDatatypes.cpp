// To know the sizes of datatypes
#include<iostream>
using namespace std ;
int main()
{
    cout<<"size of int is "<<sizeof(int)<<" in bytes\n";
    cout<<"size of long int is "<<sizeof(long int)<<" in bytes\n";
    cout<<"size of char  is "<<sizeof(char)<<" in bytes\n";
    cout<<"size of float is "<<sizeof(float)<<" in bytes\n";
    return 0 ;
}